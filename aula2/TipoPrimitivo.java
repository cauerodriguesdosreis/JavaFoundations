package aula2;

public class TipoPrimitivo {

	public static void main(String[] args) {
	
		//Exemplos de tipos de dados primitivos em Java
		
		char sexo = 'f';
		byte idade = 89;
		short codigo = 764;
		int alunos = 100, classes = 100;
		long habitantes = 9050100;
		float nota = 7.58f;
		double dolar = 5.64;
		boolean alternativa = false;
		
		System.out.println("Sexo: " + sexo + " Idade: " + idade + " Código: " + codigo);
		System.out.println("Nota: " + nota + " Aluno: " + alunos + " Classes: " + classes);
		System.out.println("Habitantes: " + habitantes + " Dólar: " + dolar + " Alternativa: " + alternativa);
	}

}
